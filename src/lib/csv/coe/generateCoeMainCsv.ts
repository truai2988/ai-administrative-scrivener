import type { CoeApplicationFormData } from '@/lib/schemas/coeApplicationSchema';
import {
  formatDateToYYYYMMDD,
  formatZipCode,
  formatPhoneNumber,
  createCsvString,
} from '../csvUtils';

/**
 * COE 申請情報入力(在留資格認定証明書交付申請).csv のデータを生成します。
 * 対象となる項目数は全84項目（インデックス 0〜83）です。
 *
 * CSV仕様ヘッダー（1行目）は政府指定の論理項目名に完全準拠。
 * row[index] = ... の明示的マッピングにより、インデックスのズレを防止。
 *
 * @param data - CoeApplicationFormData (COE申請フォームの全データ)
 * @returns CSVフォーマットの文字列 (ヘッダー1行 + データ1行)
 */
export const generateCoeMainCsv = (data: CoeApplicationFormData): string => {
  const id = data.identityInfo;

  // 84個の要素を持つ配列を初期化
  const row: string[] = new Array(84).fill('');

  // ═════════════════════════════════════════════════════════════════════════
  // 身分事項 (index 0〜36)
  // ═════════════════════════════════════════════════════════════════════════

  // [0] 身分事項_国籍・地域
  row[0] = id.nationality || '';
  // [1] 身分事項_生年月日  (YYYYMMDD)
  row[1] = id.birthDate || '';
  // [2] 身分事項_氏名  (半角英字大文字、スペース区切り)
  row[2] = id.nameEn || '';
  // [3] 身分事項_性別  (プルダウン値: 1=男, 2=女)
  row[3] = id.gender || '';
  // [4] 身分事項_出生地
  row[4] = id.birthPlace || '';
  // [5] 身分事項_配偶者の有無  (1=有, 2=無)
  row[5] = id.maritalStatus || '';
  // [6] 身分事項_職業
  row[6] = id.occupation || '';
  // [7] 身分事項_本国における居住地
  row[7] = id.homeCountryAddress || '';
  // [8] 身分事項_日本における連絡先_郵便番号 (ハイフンなし7桁)
  row[8] = formatZipCode(id.japanZipCode);
  // [9] 身分事項_日本における連絡先_居住地(都道府県)
  row[9] = id.japanPrefecture || '';
  // [10] 身分事項_日本における連絡先_居住地(市区町村)
  row[10] = id.japanCity || '';
  // [11] 身分事項_日本における連絡先_居住地(町名丁目番地号等)
  row[11] = id.japanAddressLines || '';
  // [12] 身分事項_日本における連絡先_電話番号
  row[12] = formatPhoneNumber(id.phoneNumber);
  // [13] 身分事項_日本における連絡先_携帯電話番号
  row[13] = formatPhoneNumber(id.mobileNumber);
  // [14] 身分事項_メールアドレス
  row[14] = id.email || '';
  // [15] 身分事項_旅券_(1)番号
  row[15] = id.passportNumber || '';
  // [16] 身分事項_旅券_(2)有効期限  (YYYYMMDD)
  row[16] = id.passportExpiryDate || '';
  // [17] 身分事項_入国目的  (プルダウン値)
  row[17] = id.entryPurpose || '';
  // [18] 身分事項_入国予定年月  (自由入力 40文字)
  row[18] = id.entryDate || '';
  // [19] 身分事項_上陸予定港  (プルダウン値)
  row[19] = id.entryPort || '';
  // [20] 身分事項_上陸予定港_その他
  row[20] = id.entryPurposeOther || '';
  // [21] 身分事項_滞在予定期間(年数)
  row[21] = id.stayPeriod || '';
  // [22] 身分事項_滞在予定期間(月数)
  row[22] = ''; // ※ スキーマに stayPeriodMonths フィールドなし → 空文字パディング
  // [23] 身分事項_同伴者の有無  (1=有, 2=無)
  row[23] = id.accompanyingPersons || '';
  // [24] 身分事項_査証申請予定地
  row[24] = id.visaApplicationPlace || '';
  // [25] 身分事項_過去の出入国歴_有無
  row[25] = ''; // ※ スキーマに pastEntryHistory (有無フラグ) なし → 空文字パディング
  // [26] 身分事項_過去の出入国歴_回数
  row[26] = id.pastEntryCount || '';
  // [27] 身分事項_直近の出入国歴(始期)
  row[27] = id.latestEntryDate || '';
  // [28] 身分事項_直近の出入国歴(終期)
  row[28] = id.latestDepartureDate || '';
  // [29] 身分事項_過去の在留資格認定証明書交付申請歴_有無 (1=有, 2=無)
  row[29] = id.pastApplicationRecord || '';
  // [30] 身分事項_過去の在留資格認定証明書交付申請歴_回数
  row[30] = id.pastApplicationCount || '';
  // [31] 身分事項_過去の在留資格認定証明書交付申請歴_うち不交付となった回数
  row[31] = id.pastApplicationApprovalCount || '';
  // [32] 身分事項_犯罪を理由とする処分を受けたことの有無 (1=有, 2=無)
  row[32] = id.criminalRecord || '';
  // [33] 身分事項_犯罪を理由とする処分を受けたことの有無_有_内容入力欄
  row[33] = id.criminalRecordDetail || '';
  // [34] 身分事項_退去強制又は出国命令による出国の有無 (1=有, 2=無)
  row[34] = id.departureOrderHistory || '';
  // [35] 身分事項_退去強制又は出国命令による出国の有無_回数
  row[35] = id.departureOrderCount || '';
  // [36] 身分事項_退去強制又は出国命令による出国の有無_直近の送還歴
  row[36] = id.latestDepartureOrderDate || '';

  // ═════════════════════════════════════════════════════════════════════════
  // 在日親族及び同居者 (index 37〜79)
  // 37: 有無フラグ, 38〜79: 6名 x 7項目 = 42項目
  // ═════════════════════════════════════════════════════════════════════════

  // [37] 身分事項_在日親族及び同居者_有無 (1=有, 2=無)
  row[37] = id.familyInJapan || '';

  // [38〜79] 親族6名 (各 7項目: 続柄, 氏名, 生年月日, 国籍, 同居予定, 勤務先, 在留カード番号)
  const relatives = id.relatives || [];
  for (let i = 0; i < 6; i++) {
    const offset = 38 + i * 7;
    const rel = relatives[i];
    if (rel) {
      row[offset]     = rel.relationship || '';    // 続柄
      row[offset + 1] = rel.name || '';            // 氏名
      row[offset + 2] = rel.birthDate || '';       // 生年月日 (YYYYMMDD)
      row[offset + 3] = rel.nationality || '';     // 国籍・地域
      row[offset + 4] = rel.cohabitation || '';    // 同居予定の有無 (1=有, 2=無)
      row[offset + 5] = rel.workplace || '';       // 勤務先名称・通学先名称
      row[offset + 6] = rel.residenceCardNumber || ''; // 在留カード番号
    }
    // データがない場合は初期化済みの空文字がそのまま利用される
  }

  // ═════════════════════════════════════════════════════════════════════════
  // 受領方法等・入力情報確認 (index 80〜83)
  // ═════════════════════════════════════════════════════════════════════════

  // [80] 受領方法等_在留資格認定証明書の受領方法  (プルダウン: 1=窓口, 2=郵送)
  row[80] = data.residenceCardReceiptMethod || '';
  // [81] 受領方法等_通知送信用メールアドレス
  row[81] = data.notificationEmail || '';
  // [82] 入力情報確認_申請に先立ち、申請者本人に申請の意思を確認してください。
  row[82] = data.checkIntent || '';
  // [83] 入力情報確認_フリー欄
  row[83] = data.freeFormat || '';

  // ─── ヘッダー定義（84項目厳密一致） ──────────────────────────────────
  const headers: string[] = [
    '身分事項_国籍・地域',                                // [0]
    '身分事項_生年月日',                                   // [1]
    '身分事項_氏名',                                       // [2]
    '身分事項_性別',                                       // [3]
    '身分事項_出生地',                                     // [4]
    '身分事項_配偶者の有無',                              // [5]
    '身分事項_職業',                                       // [6]
    '身分事項_本国における居住地',                        // [7]
    '身分事項_日本における連絡先_郵便番号',              // [8]
    '身分事項_日本における連絡先_居住地(都道府県)',      // [9]
    '身分事項_日本における連絡先_居住地(市区町村)',      // [10]
    '身分事項_日本における連絡先_居住地(町名丁目番地号等)', // [11]
    '身分事項_日本における連絡先_電話番号',              // [12]
    '身分事項_日本における連絡先_携帯電話番号',          // [13]
    '身分事項_メールアドレス',                            // [14]
    '身分事項_旅券_（1）番号',                            // [15]
    '身分事項_旅券_（2）有効期限',                        // [16]
    '身分事項_入国目的',                                   // [17]
    '身分事項_入国予定年月',                              // [18]
    '身分事項_上陸予定港',                                // [19]
    '身分事項_上陸予定港_その他',                         // [20]
    '身分事項_滞在予定期間(年数)',                        // [21]
    '身分事項_滞在予定期間(月数)',                        // [22]
    '身分事項_同伴者の有無',                              // [23]
    '身分事項_査証申請予定地',                            // [24]
    '身分事項_過去の出入国歴_有無',                      // [25]
    '身分事項_過去の出入国歴_回数',                      // [26]
    '身分事項_直近の出入国歴(始期)',                      // [27]
    '身分事項_直近の出入国歴(終期)',                      // [28]
    '身分事項_過去の在留資格認定証明書交付申請歴_有無',  // [29]
    '身分事項_過去の在留資格認定証明書交付申請歴_回数',  // [30]
    '身分事項_過去の在留資格認定証明書交付申請歴_うち不交付となった回数', // [31]
    '身分事項_犯罪を理由とする処分を受けたことの有無',  // [32]
    '身分事項_犯罪を理由とする処分を受けたことの有無_有_内容入力欄', // [33]
    '身分事項_退去強制又は出国命令による出国の有無',    // [34]
    '身分事項_退去強制又は出国命令による出国の有無_回数', // [35]
    '身分事項_退去強制又は出国命令による出国の有無_直近の送還歴', // [36]
    '身分事項_在日親族及び同居者_有無',                  // [37]
  ];

  // 親族6名分のヘッダー (38〜79)
  for (let i = 1; i <= 6; i++) {
    headers.push(
      `身分事項_在日親族及び同居者${i}_在日親族及び同居者_続柄`,
      `身分事項_在日親族及び同居者${i}_在日親族及び同居者_氏名`,
      `身分事項_在日親族及び同居者${i}_在日親族及び同居者_生年月日`,
      `身分事項_在日親族及び同居者${i}_在日親族及び同居者_国籍・地域`,
      `身分事項_在日親族及び同居者${i}_在日親族及び同居者_同居予定の有無`,
      `身分事項_在日親族及び同居者${i}_在日親族及び同居者_勤務先名称・通学先名称`,
      `身分事項_在日親族及び同居者${i}_在日親族及び同居者_在留カード番号_特別永住者証明書番号`,
    );
  }

  // 末尾4項目 (80〜83)
  headers.push(
    '受領方法等_在留資格認定証明書の受領方法',            // [80]
    '受領方法等_通知送信用メールアドレス',                // [81]
    '入力情報確認_申請に先立ち、申請者本人に申請の意思を確認してください。', // [82]
    '入力情報確認_フリー欄',                              // [83]
  );

  return createCsvString(headers, row);
};
